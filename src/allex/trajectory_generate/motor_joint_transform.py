GroupKind = str  # {"shoulder", "elbow", "wrist", "finger", "thumb", "identity"}


def _scalar_passthrough(values: np.ndarray, factor, exponent: int) -> np.ndarray:
    """ratio 기반 스칼라 변환: K_j_i = ratio_i^exponent · K_m_i.

    factor 는 float 또는 (N,) array 모두 허용 (브로드캐스팅).
    """
    f = np.asarray(factor, dtype=np.float64)
    out = np.asarray(values, dtype=np.float64) * (f ** exponent)
    return out


def transform_group(
    group_kind: GroupKind,
    q_rad: np.ndarray,
    *,
    k_pos: np.ndarray | None = None,
    k_vel: np.ndarray | None = None,
    trq_lim: np.ndarray | None = None,
) -> dict[str, np.ndarray]:
    """모터-단 PD/토크 한계를 관절-단으로 변환.

    Args:
        group_kind: 변환 종류 ("shoulder"/"elbow"/"wrist"/"finger"/"thumb"/"identity").
        q_rad: 현재 관절각 [rad]. 길이 N (group의 자유도).
            - shoulder: 길이 1~3 (각 관절 독립). 비선형 자코비안 미사용.
            - elbow: 길이 2 (Elbow + Wrist_Yaw). 자코비안은 q 무관.
            - wrist: 길이 2 (Roll, Pitch). 자코비안은 q 의존.
            - finger/thumb: 길이 3. 자코비안은 q 의존.
            - identity: 길이 N (변환 없이 통과).
        k_pos / k_vel / trq_lim: 모터-단 입력 (길이 N). NaN 가능, 미입력 가능.

    Returns:
        dict with same keys as inputs that were provided. Values are joint-단 N개.
        NaN 마스크 보존 (입력 NaN → 출력 NaN).
    """
    q_rad = np.asarray(q_rad, dtype=np.float64)
    out: dict[str, np.ndarray] = {}

    if group_kind == "identity":
        if k_pos is not None:
            out["k_pos"] = np.asarray(k_pos, dtype=np.float64).copy()
        if k_vel is not None:
            out["k_vel"] = np.asarray(k_vel, dtype=np.float64).copy()
        if trq_lim is not None:
            out["trq_lim"] = np.asarray(trq_lim, dtype=np.float64).copy()
        return out

    if group_kind == "shoulder":
        # 관절별 독립 스칼라 변환. q 무관.
        if k_pos is not None:
            out["k_pos"] = _scalar_passthrough(np.asarray(k_pos), SHOULDER_RATIO, 2)
        if k_vel is not None:
            out["k_vel"] = _scalar_passthrough(np.asarray(k_vel), SHOULDER_RATIO, 2)
        if trq_lim is not None:
            out["trq_lim"] = _scalar_passthrough(np.asarray(trq_lim), SHOULDER_RATIO, 1)
        return out

    if group_kind == "waist":
        # 관절별 다른 감속비. joint_1=50, joint_2=25.
        if q_rad.shape[0] != WAIST_RATIOS.shape[0]:
            raise ValueError(f"waist expects {WAIST_RATIOS.shape[0]} joints, got {q_rad.shape[0]}")
        if k_pos is not None:
            out["k_pos"] = _scalar_passthrough(np.asarray(k_pos), WAIST_RATIOS, 2)
        if k_vel is not None:
            out["k_vel"] = _scalar_passthrough(np.asarray(k_vel), WAIST_RATIOS, 2)
        if trq_lim is not None:
            out["trq_lim"] = _scalar_passthrough(np.asarray(trq_lim), WAIST_RATIOS, 1)
        return out

    if group_kind == "neck":
        # 관절별 다른 감속비. joint_1=Neck_Pitch=7.75, joint_2=Neck_Yaw=10.
        if q_rad.shape[0] != NECK_RATIOS.shape[0]:
            raise ValueError(f"neck expects {NECK_RATIOS.shape[0]} joints, got {q_rad.shape[0]}")
        if k_pos is not None:
            out["k_pos"] = _scalar_passthrough(np.asarray(k_pos), NECK_RATIOS, 2)
        if k_vel is not None:
            out["k_vel"] = _scalar_passthrough(np.asarray(k_vel), NECK_RATIOS, 2)
        if trq_lim is not None:
            out["trq_lim"] = _scalar_passthrough(np.asarray(trq_lim), NECK_RATIOS, 1)
        return out

    # 자코비안 기반 그룹.
    if group_kind == "elbow":
        if q_rad.shape[0] != 2:
            raise ValueError(f"elbow expects 2 joints, got {q_rad.shape[0]}")
        J = elbow_jacobian()
    elif group_kind == "wrist":
        if q_rad.shape[0] != 2:
            raise ValueError(f"wrist expects 2 joints, got {q_rad.shape[0]}")
        # CSV 매핑: q_rad[0] = Wrist_Roll, q_rad[1] = Wrist_Pitch
        J = wrist_jacobian(qr=q_rad[0], qp=q_rad[1])
    elif group_kind == "finger":
        if q_rad.shape[0] != 3:
            raise ValueError(f"finger expects 3 joints, got {q_rad.shape[0]}")
        J = finger_jacobian(q_rad[0], q_rad[1], q_rad[2])
    elif group_kind == "thumb":
        if q_rad.shape[0] != 3:
            raise ValueError(f"thumb expects 3 joints, got {q_rad.shape[0]}")
        J = thumb_jacobian(q_rad[0], q_rad[1], q_rad[2])
    else:
        raise ValueError(f"unknown group_kind: {group_kind!r}")

    if k_pos is not None:
        out["k_pos"] = gain_transform_diag(J, np.asarray(k_pos))
    if k_vel is not None:
        out["k_vel"] = gain_transform_diag(J, np.asarray(k_vel))
    if trq_lim is not None:
        out["trq_lim"] = torque_limit_transform(J, np.asarray(trq_lim))
    return out


