def gain_transform_diag(J: np.ndarray, k_motor: np.ndarray) -> np.ndarray:
    """K_j_full = J^T · diag(k_motor) · J 의 대각만 반환 (CSV 관절별 스칼라용).

    NaN 입력은 NaN 출력으로 전파 (마스크 보존).
    """
    k_motor = np.asarray(k_motor, dtype=np.float64)
    if np.any(np.isnan(k_motor)):
        return np.full(J.shape[1], np.nan, dtype=np.float64)
    K_j_full = J.T @ np.diag(k_motor) @ J
    return np.diag(K_j_full).copy()


def torque_limit_transform(J: np.ndarray, tau_motor_max: np.ndarray) -> np.ndarray:
    """관절 토크 한계 = Σ_k |J[k,i]| · τ_motor_max[k]  (보수적 절대값 합산)."""
    tau_motor_max = np.asarray(tau_motor_max, dtype=np.float64)
    if np.any(np.isnan(tau_motor_max)):
        return np.full(J.shape[1], np.nan, dtype=np.float64)
    return np.abs(J).T @ tau_motor_max


