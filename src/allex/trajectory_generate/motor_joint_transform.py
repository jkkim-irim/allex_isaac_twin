def elbow_jacobian() -> np.ndarray:
    """차동풀리 상수 자코비안 (좌우 동일).

        m0 =  q3/(n1·n2)  +  q4/(n1·n2·n3)
        m1 = -q3/(n1·n2)  +  q4/(n1·n2·n3)

    Returns: (2,2) numpy array.
    """
    a = 1.0 / (ELBOW_N1 * ELBOW_N2)
    b = 1.0 / (ELBOW_N1 * ELBOW_N2 * ELBOW_N3)
    return np.array([[a, b], [-a, b]], dtype=np.float64)


